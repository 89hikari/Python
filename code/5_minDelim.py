# 5) Какое самое маленькое число делится нацело на все числа от 1 до 20?
dat = 2520  # Максимально неоптимально
flag = 0
r = (3, 4, 6, 7, 8, 9, 11, 12, 13, 14, 15, 16, 17, 18, 19)
while True:
    dat += 10
    for i in r:
        if dat % i == 0:
            flag += 1
    if flag == 15:
        break
    flag = 0
print(dat)
